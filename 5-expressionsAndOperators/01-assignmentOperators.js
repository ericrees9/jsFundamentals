// Assignment Operator
let x = 5;

// Addition Assignment 
let x = x + 1;
    x += 1;
// same way to write the different functions

// Subtration Assingment
let x = x - 1;
    x -= 1;

// Multiplication Assingment
let x = x * 1;
    x *= 1;

// Division Assignment
let x = x / 1;
    x /= 1;

// Remainder/Modulus Assignment
// When we devide one number by another that isn't perfect. Remainder assignment gives us what's left over.
let x = x % 1;
    x %= 1;

// Exponential Assignmnent
let x = x ** 1;
    x **= 1;



