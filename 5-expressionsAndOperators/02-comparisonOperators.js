// * Comparison Operators

// equal
"3" == 3;
// - true

// strictly equal
"3" === 3;
// - false
3 === 3;
// - true

// does not equal
"3" != 4;

// strictly not equal
"3" !== 3;

// greater than
3 > 2;

// less than
2 < 3;

// greater than or equal to
3 >= 2;

// less than or equal to
2 <= 3; 

// And
// Checking if one thing and another are the same
2 && 3;

// Or
// Checking if one thing or another is true
2 || 3;

